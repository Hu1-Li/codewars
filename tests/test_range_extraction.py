# coding=utf-8
from solutions.range_extraction import solution
import unittest


class RangeExtractionTestCase(unittest.TestCase):
    def test_simple(self):
        fixtures = [
            ([-6, -3, -2, -1, 0, 1, 3, 4, 5, 7, 8, 9, 10, 11, 14, 15, 17, 18, 19, 20], "-6,-3-1,3-5,7-11,14,15,17-20"),
            ([-3, -2, -1, 2, 10, 15, 16, 18, 19, 20], "-3--1,2,10,15,16,18-20"),
            ([1, 3, 5, 7], "1,3,5,7"),
            ([1, 2, 4, 6], "1,2,4,6"),
            ([1, 2, 3, 5, 6, 7, 9, 10, 11, 15, 16], "1-3,5-7,9-11,15,16"),
            ([-73, -33, -32, -31, -28, -26, -24, -21, -20, -19, -18, -15], "-73,-33--31,-28,-26,-24,-21--18,-15"),
            ([1, 2, 3], "1-3"),
            ([-71, -70, -67, -64, -62, -60, -59, -58, -56, -55, -54, -51], "-71,-70,-67,-64,-62,-60--58,-56--54,-51"),
        ]

        for q, a in fixtures:
            self.assertEqual(
                solution(q),
                a,
            )
